package pack1;

public class Method_Ref {

	public static void main(String[] args) {

		// Addition of two numbers
		int a = 98;
		int b = 23;
		int result = a + b;
		System.out.println(result);
		System.out.println("Addition:"+result);

		// Sub of two numbers
		int a1 = 98;
		int b1 = 23;
		int result1 = a1 - b1;
		System.out.println(result1);
		System.out.println("Sub:"+result1);

	}

}
